def add(x, y):
    print('Adding two numbers {} & {} '.format(x, y))
    res = x + y
    return res


def mod(x, y):
    print('Mod two numbers {} & {} '.format(x, y))
    res = x % y
    return res


def product(x, y):
    print('Mul two numbers {} & {} '.format(x, y))
    res = x * y
    return res
